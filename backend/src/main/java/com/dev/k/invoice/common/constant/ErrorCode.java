package com.dev.k.invoice.common.constant;

/**
 * エラーコード定義（Enum）
 *
 * 【役割】
 * - アプリケーション内で発生するエラー種別を一元管理する
 * - 例外発生時に一貫したエラーコードを返却するために使用
 *
 * 【メリット】
 * - フロント側でエラー種別ごとの分岐処理が可能
 * - 文字列のハードコーディングを防ぎ、型安全に扱える
 * - エラー仕様の統一・保守性向上
 */
public enum ErrorCode {

	/**
     * 対象データが存在しない場合
     * 例：IDで検索したが該当レコードがない
     */
    NOT_FOUND,
    
    /**
     * 入力値のバリデーションエラー
     * 例：必須項目未入力、フォーマット不正など
     */
    VALIDATION_ERROR,
    
    /**
     * 過剰支払いエラー
     * 例：請求金額を超える支払いが行われた場合
     */
    OVER_PAYMENT,
    
    /**
     * 不正なステータス遷移
     * 例：未支払い → 完了を飛ばして変更しようとした場合など
     */
    INVALID_STATUS,
    
    /**
     * サーバ内部エラー
     * 例：予期しない例外、システム障害など
     */
    INTERNAL_ERROR

}